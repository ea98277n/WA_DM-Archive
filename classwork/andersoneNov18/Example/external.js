function addNumbers() {
    
    var num1 = parseInt(prompt("Enter number 1","3"));
    var num2 = parseInt(prompt("Enter number 3","67"));
    
    document.write("Number 1 is: " + num1 + "<br>");
    document.write("Number 2 is: " + num2 + "<br>");
    document.write("The total is: " + (num1+num2));
}